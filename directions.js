// Direction data by language
const directions = {
  en: directionsEn,
  te: directionsTe,
  hi: directionsHi,
}

// Get directions function
function getDirections(platform, position, destination, accessibility, language = "en") {
  if (!platform || !position || !destination || !accessibility) return null

  // Create a key that includes accessibility
  const key = `${platform}_${position}_${destination}_${accessibility}`

  // First try to get specific directions with accessibility
  let result = directions[language][key]

  // If not found, fall back to the general directions without accessibility
  if (!result) {
    const generalKey = `${platform}_${position}_${destination}`
    result = directions[language][generalKey]

    // If we found general directions, append accessibility-specific instructions
    if (result && accessibility) {
      const accessibilityNote = getAccessibilityNote(accessibility, language)
      if (accessibilityNote) {
        result += "\n" + accessibilityNote
      }
    }
  }

  return result || null
}

// Get accessibility-specific notes
function getAccessibilityNote(accessibility, language = "en") {
  const notes = {
    en: {
      stairs: "This route includes stairs.",
      lifts: "Lifts are available along this route for accessibility.",
      escalator: "Escalators are available along this route.",
      ramps: "Wheelchair accessible ramps are available along this route.",
    },
    te: {
      stairs: "   .",
      lifts: "      .",
      escalator: "    .",
      ramps: "      .",
    },
    hi: {
      stairs: "     ",
      lifts: "        ",
      escalator: "     ",
      ramps: "       ",
    },
  }

  return notes[language] && notes[language][accessibility] ? notes[language][accessibility] : null
}

